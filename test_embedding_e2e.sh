# 获取当前队列状态
echo -e "\n[4/8] Current embedding queue status..."
BEFORE=$(curl -s http://localhost:8083/v1/embedding/tasks/status)
echo "$BEFORE" | jq
COMPLETED_BEFORE=$(echo "$BEFORE" | jq -r '.completed')

# 上传测试frame
echo -e "\n[5/8] Uploading test frame..."
RESULT=$(python3 << 'PYEOF'
import time, uuid, secrets
ts_ms = int(time.time() * 1000)
ts_int = ts_ms << 16
rand_a = secrets.randbits(12)
rand_b = secrets.randbits(62)
uuid_int = (ts_int | 0x7000 | rand_a) << 64 | (0x8000000000000000 | rand_b)
capture_id = str(uuid.UUID(int=uuid_int))
timestamp = time.strftime('%Y-%m-%dT%H:%M:%SZ', time.gmtime())
print(f'{capture_id}|{timestamp}')
PYEOF
)
CAPTURE_ID=$(echo "$RESULT" | cut -d'|' -f1)
TIMESTAMP=$(echo "$RESULT" | cut -d'|' -f2)

RESPONSE=$(curl -s -X POST http://localhost:8083/v1/ingest \
    -F "capture_id=$CAPTURE_ID" \
    -F "file=@/tmp/test_frame.jpg;type=image/jpeg" \
    -F "metadata={\"timestamp\":\"$TIMESTAMP\",\"app_name\":\"EmbeddingTest\",\"window_name\":\"Test Window\",\"monitor_index\":0,\"capture_trigger\":\"manual\"}")

echo "$RESPONSE" | jq
FRAME_ID=$(echo "$RESPONSE" | jq -r '.frame_id')

if [ "$FRAME_ID" == "null" ] || [ -z "$FRAME_ID" ]; then
    echo "❌ Failed to upload frame"
    exit 1
fi
echo "✅ Uploaded frame_id: $FRAME_ID"

# 等待embedding完成
echo -e "\n[6/8] Waiting for embedding completion (max 30s)..."
for i in {1..30}; do
    STATUS=$(curl -s http://localhost:8083/v1/embedding/tasks/status)
    PENDING=$(echo "$STATUS" | jq -r '.pending')
    PROCESSING=$(echo "$STATUS" | jq -r '.processing')
    if [ "$PENDING" == "0" ] && [ "$PROCESSING" == "0" ]; then
        echo "✅ All tasks completed"
        break
    fi
    echo "  [$i] pending=$PENDING, processing=$PROCESSING"
    sleep 1
done

# 最终状态
echo -e "\n[7/8] Final queue status..."
AFTER=$(curl -s http://localhost:8083/v1/embedding/tasks/status)
echo "$AFTER" | jq
COMPLETED_AFTER=$(echo "$AFTER" | jq -r '.completed')
FAILED=$(echo "$AFTER" | jq -r '.failed')

if [ "$COMPLETED_AFTER" -gt "$COMPLETED_BEFORE" ]; then
    echo "✅ New embeddings: $((COMPLETED_AFTER - COMPLETED_BEFORE))"
fi

# 测试搜索
echo -e "\n[8/8] Testing search..."

echo "  → Vector search:"
VECTOR=$(curl -s "http://localhost:8083/v1/search?q=test&mode=vector&limit=3")
echo "$VECTOR" | jq '.pagination.total'

echo "  → Hybrid search:"
curl -s "http://localhost:8083/v1/search?q=embedding&mode=hybrid&limit=3" | jq '.data[0].content | {frame_id, hybrid_score, app_name}'

echo "  → Similar frames for frame_id=$FRAME_ID:"
curl -s "http://localhost:8083/v1/frames/$FRAME_ID/similar?limit=3" | jq '.similar_frames | length'

rm -f /tmp/test_frame.jpg

echo -e "\n=== Test Complete ==="
echo "Summary:"
echo "  - Frame uploaded: $FRAME_ID"
echo "  - Embeddings in DB: $COMPLETED_AFTER"
echo "  - Failed: $FAILED"
